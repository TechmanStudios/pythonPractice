import requests
from bs4 import BeautifulSoup

def extract_and_save_article(urls, output_filename):
    all_articles = []

    for url in urls:
        # Fetch the webpage content
        response = requests.get(url)
        if response.status_code != 200:
            print(f"Failed to retrieve {url}. Status code: {response.status_code}")
            continue  # Skip this URL and move to the next one

        # Parse the HTML content
        soup = BeautifulSoup(response.text, 'html.parser')

        # Extract the article title
        title_tag = soup.find("h1", class_="entry-title")
        title = title_tag.get_text(strip=True) if title_tag else "No Title Found"

        # Extract the main article content
        entry_content = soup.find("div", class_="entry-content")
        if not entry_content:
            print(f"No article content found for {url}.")
            continue

        article_text = []
        for element in entry_content.find_all(["p", "h2", "h3", "h4", "ul", "ol", "li"], recursive=False):
            if element.name == "p":
                article_text.append(element.get_text(strip=True))
            elif element.name in ["h2", "h3", "h4"]:
                header_level = int(element.name[1])
                article_text.append(f"{'#' * header_level} {element.get_text(strip=True)}")
            elif element.name in ["ul", "ol"]:
                for li in element.find_all("li"):
                    prefix = "-" if element.name == "ul" else "1."
                    article_text.append(f"{prefix} {li.get_text(strip=True)}")

        article_body = "\n\n".join(article_text)
        all_articles.append(f"# {title}\n\n{article_body}\n\n---\n\n")

    # Save to a Markdown file
    with open(output_filename, 'w', encoding='utf-8') as file:
        file.writelines(all_articles)

    print(f"All articles have been saved to '{output_filename}'.")

